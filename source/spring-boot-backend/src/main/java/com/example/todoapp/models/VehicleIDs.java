package com.example.todoapp.models;

public class VehicleIDs {

	private String vehicle_id;
	
	private String belongs_to;
	
	private String at_source;

	public String getVehicle_id() {
		return vehicle_id;
	}

	public void setVehicle_id(String vehicle_id) {
		this.vehicle_id = vehicle_id;
	}

	public String getBelongs_to() {
		return belongs_to;
	}

	public void setBelongs_to(String belongs_to) {
		this.belongs_to = belongs_to;
	}

	public String getAt_source() {
		return at_source;
	}

	public void setAt_source(String at_source) {
		this.at_source = at_source;
	}
	
	
}
